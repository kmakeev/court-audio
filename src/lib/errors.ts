/** Извлечь строковое представление ошибки (совместимо с describeError экранов). */
export function errorText(e: unknown): string {
  if (typeof e === 'string') return e;
  if (e instanceof Error) return e.message;
  return 'неизвестная ошибка';
}

/**
 * Человекочитаемый текст ошибки ядра для оператора. Если ошибка узнаётся по
 * словарю — возвращаем понятную формулировку, иначе — исходный текст (как есть).
 */
export function humanizeError(e: unknown): string {
  const raw = errorText(e);
  const lower = raw.toLowerCase();
  const hit = DICTIONARY.find((entry) => lower.includes(entry.match));
  return hit ? hit.message : raw;
}
